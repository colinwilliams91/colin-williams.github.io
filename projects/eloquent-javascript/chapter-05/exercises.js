// SOLVED with .REDUCE
function flatten(arrays) {
  let output = arrays.reduce(function(flat, current){
    return flat.concat(current);
  }, []); 
  return output;
}

// /////////////////////////////////////////////////////////////////////////////
// loop ////////////////////////////////////////////////////////////////////////
// /////////////////////////////////////////////////////////////////////////////

function loop(start, test, update, body) {
  for (let value = start; test(value); value = update(value)){
    body(value);
  }
}

